public class Calcul {

	/**
	 * @param a
	 * @param b
	 * @return l'addition de deux entiers
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	
	/**
	 * @param a
	 * @param b
	 * @return la soustraction de deux entiers
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
